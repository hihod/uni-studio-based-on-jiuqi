package com.jiuqi.rpa.widgets.uipicker;

public class UITreeNodeProperty {
	private String key;
	private String value;

	public UITreeNodeProperty(String key, String value) {
		this.key = key;
		this.value = value;
	}

	public String getKey() {
		return key;
	}

	public String getValue() {
		return value;
	}
}
